package data

// TreeRow holds one row of a tree graphic: 1 attribute byte + 8 pixel bytes.
// This matches the original Z80 format where each tree row is 9 bytes.
type TreeRow struct {
	Attr   byte
	Pixels [8]byte
}

// TreeGraphic holds 18 rows of tree data (large trees natively, small trees
// padded with sky/ground blank rows to fill 18).
type TreeGraphic struct {
	Rows [18]TreeRow
}

// TreeSpriteEntry matches the original sprite table at $6D73.
// Type 0 = large tree (18 rows of real data).
// Type 1 = small tree (6 rows of real data, padded to 18).
type TreeSpriteEntry struct {
	Type    byte // 0=large, 1=small
	Graphic TreeGraphic
}

// blankSkyRow returns a row with the given attribute and all-zero pixels.
func blankSkyRow(attr byte) TreeRow {
	return TreeRow{Attr: attr}
}

// buildSmallTree pads 6 rows of small-tree data into 18 rows:
// 5 blank sky rows (attr=$2C) + 6 data rows + 7 blank ground rows (attr=$20).
// This replicates the original's temp buffer layout at $7BFA.
func buildSmallTree(rows [6]TreeRow) TreeGraphic {
	var g TreeGraphic
	// 5 blank sky rows
	for i := 0; i < 5; i++ {
		g.Rows[i] = blankSkyRow(0x2C)
	}
	// 6 data rows
	for i := 0; i < 6; i++ {
		g.Rows[5+i] = rows[i]
	}
	// 7 blank ground rows
	for i := 0; i < 7; i++ {
		g.Rows[11+i] = blankSkyRow(0x20)
	}
	return g
}


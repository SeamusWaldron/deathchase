package screen

// PrintChar draws a single character from the ZX Spectrum ROM font
// at the given display file address.
// The ROM character set starts at $3C00, each character is 8 bytes.
func (b *Buffer) PrintChar(addr uint16, ch byte) {
	charData := ROMFont[int(ch)*8 : int(ch)*8+8]
	for i := 0; i < 8; i++ {
		b.DrawByteOverwrite(addr, charData[i])
		addr = NextScanLine(addr)
	}
}

// PrintString draws a string at the given display address.
// Replicates the routine at $6949.
func (b *Buffer) PrintString(addr uint16, s string) {
	for _, ch := range s {
		b.PrintChar(addr, byte(ch))
		addr++ // Next column
	}
}

